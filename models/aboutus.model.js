const mongoose = require("mongoose");

const aboutusSchema = new mongoose.Schema(
  {
    image: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    isActive: {
      type: Boolean,
      required: true,
      default: true,
    },
  },
  { timestamps: true }
);

const Aboutus = mongoose.model("aboutus", aboutusSchema);

module.exports = Aboutus;

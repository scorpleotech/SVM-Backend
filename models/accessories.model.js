// models/Accessory.js
const mongoose = require("mongoose");

const AccessorySchema = new mongoose.Schema(
  {
    couponCode: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("Accessory", AccessorySchema);
